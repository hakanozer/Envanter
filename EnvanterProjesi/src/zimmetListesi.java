        int coz = Integer.valueOf("" + jTable1.getValueAt(selected, 3));
        jComboBox6.setSelectedItem(coz);
        jComboBox1.setSelectedItem(il);
        ilceDoldur();
        jComboBox2.setSelectedItem(ilce);
        subeDoldur();
        jComboBox3.setSelectedItem(jTable1.getValueAt(selected, 1));
        personelDoldur();
        jComboBox4.setSelectedItem(jTable1.getValueAt(selected, 2));
        
