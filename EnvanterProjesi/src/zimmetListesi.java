    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased

        DefaultTableModel dt = new DefaultTableModel();
        dt.addColumn("Zimmet Adı");
        dt.addColumn("Şube");
        dt.addColumn("Personel");
        dt.addColumn("Adet");
        dt.addColumn("Tarih");
        jTable1.setModel(dt);
        DB db = new DB();
        try {

            ResultSet rs = db.baglan().executeQuery("Select * from zimmetler left join personeller on personeller.personel_id=zimmetler.zim_personel_id left join subeler on zimmetler.zim_sube_id=subeler.sube_id left join urunler on zimmetler.zim_urun_id=urunler.urun_id where urun_adi like '%" + jTextField1.getText() + "%'");
            while (rs.next()) {
                zimIdler.add(rs.getString("zim_id"));
                dt.addRow(new String[]{rs.getString("urun_adi"), rs.getString("sube_adi"), rs.getString("personel_adi") + " " + rs.getString("personel_soyadi"), rs.getString("zim_urun_adet"), rs.getString("zim_tarih")});

            }
            jTable1.setModel(dt);
            db.kapat();
        } catch (Exception e) {

            System.err.println("Tablo doldurma hatası: " + e);

        }


    }//GEN-LAST:event_jTextField1KeyReleased

