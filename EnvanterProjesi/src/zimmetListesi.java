    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        System.out.println(jComboBox3.getSelectedIndex());
        if (jComboBox2.getSelectedIndex() >= 0 && jComboBox3.getSelectedIndex() >= 0 && jComboBox4.getSelectedIndex() >= 0) {
            DB db = new DB();
            int selected = jTable1.getSelectedRow();
            String urun = urId.get(jComboBox5.getSelectedIndex());
            String adet = jComboBox6.getSelectedItem() + "";
            String sube = subeId.get(jComboBox3.getSelectedIndex());
            String pers = perId.get(jComboBox4.getSelectedIndex());

            if (selected >= 0) {

                Object[] options = {"Evet", "Hayır"};
                int response = JOptionPane.showOptionDialog(this, "Seçili kayıtı değiştirmek istediğinize emin misiniz?", "Düzenleme İşlemi",
                        JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE, null,
                        options, options[0]);
                if (response == JOptionPane.YES_OPTION) {
                    String zim = zimIdler.get(selected);
                    try {

                        int res = db.baglan().executeUpdate("Update zimmetler set zim_urun_id=" + urun + " ,zim_sube_id=" + sube + ", zim_personel_id=" + pers + ",zim_urun_adet=" + Integer.parseInt(adet) + " where zim_id=" + zim);
                        if (res > 0) {

                            UIManager.put("OptionPane.okButtonText", "Tamam");
                            JOptionPane.showMessageDialog(this, "Düzenleme Başarılı");

                        }
                        db.kapat();
                        tabloDoldur();                     
                    } catch (Exception e) {

                        System.err.println("Update hatası: " + e);

                    }
                }
            } else {

                UIManager.put("OptionPane.okButtonText", "Tamam");
                JOptionPane.showMessageDialog(this, "Lütfen değiştirmek istediğiniz kayıtı tablodan seçin.");

            }
        } else {

            UIManager.put("OptionPane.okButtonText", "Tamam");
            JOptionPane.showMessageDialog(this, "Lütfen bütün alanları doldurduğunuza emin olun.");

        }
    }//GEN-LAST:event_jButton5ActionPerformed

